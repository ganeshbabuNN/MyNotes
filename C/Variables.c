#include <stdio.h>
#include <stdlib.h>

int main(void) {
	float priceOfGasInCalifornia = 0.99; //Sometime last century...
	int y = 0;
	short a = 1;
	double radius = 2.34567;
	y=12;

	printf("priceOfGasInCalifornia: %f\n",priceOfGasInCalifornia);
	printf("y: %d\n",y);
	printf("a: %d\n",a);
	printf("radius: %lf\n",radius);

	return EXIT_SUCCESS;
}


