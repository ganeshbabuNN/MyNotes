using System;      
public class IfExample  
    {  
        public static void Main(string[] args)  
        {  
            int num = 11;  
            if (num % 2 == 0)  
            {  
                Console.WriteLine("It is even number");  
            }  
            else  
            {  
                Console.WriteLine("It is odd number");  
            }  
              
        }  
    }  