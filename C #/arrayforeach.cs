using System;  
public class ArrayExample  
{  
    public static void Main(string[] args)  
    {  
        int[] arr = { 10, 20, 30, 40, 50 };//creating and initializing array  
   
        //traversing array  
        foreach (int i in arr)  
        {  
            Console.WriteLine(i);  
        }  
    }  
}  